     * @return \Illuminate\Http\Response
     */
     public function store(Request $request)
     {
         $profile = $request->user()->profile;
         $dp = Dp::make($request->all());
         $profile->dp()->save($dp);

         $profile = $request->user()->profile;

         return response()->json($profile->load(config('profile.load')));
     }

